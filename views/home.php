    <h3>Some informations about Nonograms...</h3>
    <p>Nonograms are a type of puzzles on a grid, derivated from another famous grid puzzle: crosswords. Early Crosswords often used pictures as part of their layout: an example is Stephenson's Train, published in Italy in 1925. However nonograms are fairly newer than crosswords, as their origin is found in 1987, in Tokyo.</p>
    <p>Their creator, Graphics editor Non Ishida, got the idea after winning a competition to design a picture created by having certain lights on or off in a skyscraper: a puzzle based around filling in certain squares in a grid. The following year, she published only three picture grid puzzles in Japan using the name "Window Art Puzzles", and coincidentally Mr Tetsuya Nishio also published this type of puzzle in a different magazine the same year.</p>
    <p>In 1989, James Dalgety discovers this new puzzle form and make an arrangement with Non Ishida to commercialise her designs throughout the world outside Japan, inventing the name Nonogram after "Non" Ishida and Dia"gram".</p>
    <p>They were an immediate success and are still widely spread today.</p>
